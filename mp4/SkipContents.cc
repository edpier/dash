#include "SkipContents.h"


/******************************************************************************//**
*
**********************************************************************************/
SkipContents::SkipContents(const Type& type, uint32_t size)
            : Contents(type, size) {
    
    this->nbytes = size-8;
    data = new unsigned char[nbytes];
    
    
} // end of constructor
    
    
/******************************************************************************//**
*
**********************************************************************************/
SkipContents::~SkipContents() {
    
    delete [] data;
    
} // end of destructor

    
/******************************************************************************//**
*
**********************************************************************************/
int SkipContents::getByteCount() const {  return nbytes; }
    
/******************************************************************************//**
*
**********************************************************************************/uint8_t* SkipContents::getBytes() { return data; }
          
/******************************************************************************//**
*
**********************************************************************************/
const uint8_t* SkipContents::getBytes() const { return data; }
    

/******************************************************************************//**
*
**********************************************************************************/
void SkipContents::read(ByteSource& source) {
    
    source.read(data, nbytes);
    
} // end of read method
